-- ------------------------------------------------------------
-- 1. Таблица симптомов
-- ------------------------------------------------------------
CREATE TABLE symptoms (
    id          VARCHAR(3)   PRIMARY KEY,
    question    TEXT         NOT NULL
);

-- ------------------------------------------------------------
-- 2. Таблица диагнозов
-- ------------------------------------------------------------
CREATE TABLE diagnoses (
    id                VARCHAR(3)    PRIMARY KEY,
    name              TEXT          NOT NULL,
    prior_probability NUMERIC(4,2)  NOT NULL,
    CONSTRAINT chk_prior CHECK (prior_probability >= 0 AND prior_probability <= 1)
);

-- ------------------------------------------------------------
-- 3. Таблица связей «диагноз – симптом»
-- ------------------------------------------------------------
CREATE TABLE diagnosis_symptom (
    diagnosis_id   VARCHAR(3)   REFERENCES diagnoses(id),
    symptom_id     VARCHAR(3)   REFERENCES symptoms(id),
    p_s_given_d    NUMERIC(4,2) NOT NULL,
    p_s_given_not_d NUMERIC(4,2) NOT NULL,
    PRIMARY KEY (diagnosis_id, symptom_id),
    CONSTRAINT chk_probs CHECK (
        p_s_given_d >= 0 AND p_s_given_d <= 1 AND
        p_s_given_not_d >= 0 AND p_s_given_not_d <= 1
    )
);

